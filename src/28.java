public class MathProblems {
    public static void main(String[] args) {
        // Example Java code with basic arithmetic operations and loops

        System.out.println("This is a simple program to perform basic arithmetic operations.");
        System.out.println("First: " + (5 + 3));
        System.out.println("Second: " + (2 * 4));
        System.out.println("Third: " + (10 / 3)); // Division
        System.out.println("Fourth: " + (6 - 2)); // Subtraction

        // Adding some numbers using for loop
        int count = 5;
        for (int i = 0; i < count; i++) {
            System.out.println(i);
        }
    }
}
